    "συμβόλαια, ατζέντες και ανοχή";
}

function get_story_people_1_en() {
  return <<<EOHTML

<p>
For a compiler presentation,
my friends and I met daily in-person
and we worked together 4 to 8 hours at a time,
for about 3 months.
</p>
<p>
I've been convinced that by constantly keeping in contact,
a team can keep a to-do list in their mind,
as each member will recall some of it.
Plus they're all up to date with each other's opinion,
as their opinions change and refine.
</p>

EOHTML;
}

function get_story_people_1_gr() {
  return <<<EOHTML

<p>
Για μια
παρουσίαση στο μάθημα των μεταγλωττιστών,
συνεργαστήκαμε με τους φίλους μου
πρόσωπο με πρόσωπο 4 με 8 ώρες καθημερινά
για περίπου 3 μήνες.
</p>
<p>
Έχω πειστεί ότι με αρκετά καλή επικοινωνία,
οι λίστες και τα συμβόλαια γίνονται δευτερεύοντα.
Μια ομάδα μπορεί να απομνημονεύσει τις υποχρεώσεις της
μέσα από το διάλογο, και να παραμείνει ενήμερη και ενεργή
στις τάσεις στο εργασιακό περιβάλλον.
</p>

EOHTML;
}

function get_story_people_2_en() {
  return <<<EOHTML

<p>
I would like to sum up the mindset as:
there are only 2 groups of people in a project,
the technically literate ones and the technically illiterate ones.
But 2 groups of people are far too many and so they will fail.
To solve this, they can cooperate so much that they merge into one.
If someone gains so much trust that they
unexpectedly specialize as a manager,
they can be labeled a manager, although it doesn't matter.
</p>
<p>
I'm aware all of this <i>is done</i>,
and it's documented and talked about.
One day I'll work in such a place.
</p>

EOHTML;
}

function get_story_people_2_gr() {
  return <<<EOHTML

<p>
Θα συνόψιζα ως εξής:
σε ένα έργο, μπορεί κανείς να διαχωρίσει τα άτομα που
συμμετέχουν σε δύο μόνο κατηγορίες, αυτές είναι
τα άτομα που έχουν γνώση πληροφορικής και αυτά που δεν έχουν.
Δεν υπάρχουν ρόλοι εκτός αν προκύψουν.
Δύο ομάδες όμως είναι πάρα πολλές και θα αποτύχει το έργο,
οπότε η λύση είναι να συνεργαστούν τόσο ώστε να γίνουν ένα σώμα.
</p>
<p>
Γνωρίζω ότι αυτά <i>γίνονται</i>.
Μια μέρα και εγώ θα εργάζομαι σε τέτοιο περιβάλλον.
</p>

EOHTML;
}

