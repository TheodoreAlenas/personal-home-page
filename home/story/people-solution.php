function get_story_people_solution_title_en() {
  return
    "Human communication is " .
    "the only solution I believe in";
}
function get_story_people_solution_title_gr() {
  return
    "Η ανθρώπινη επικοινωνία είναι " .
    "η μόνη λύση που εμπιστεύομαι";
}

function get_story_people_solution_1_en() {
  return <<<EOHTML

<p>
For a compiler presentation,
my friends and I met daily in-person
and we worked together 4 to 8 hours at a time,
for about 3 months.
It was an amazing experience.
</p>
<p>
I have no doubt anymore that most problems
are solved through better communication.
I also can't be convinced that there are
people pinning down how you work,
perhaps nobody thought to talk to them.
At least I hope so.
</p>

EOHTML;
}

function get_story_people_solution_1_gr() {
  return <<<EOHTML

<p>
Για μια
παρουσίαση στο μάθημα των μεταγλωττιστών,
συνεργαστήκαμε με τους φίλους μου
πρόσωπο με πρόσωπο 4 με 8 ώρες καθημερινά
για περίπου 3 μήνες.
Ήταν καταπλητική εμπειρία.
</p>
<p>
Δεν αμφιβάλω πλέον ότι τα περισσότερα προβλήματα
λύνονται με καλύτερη επικοινωνία.
Επίσης δεν μπορώ να πειστώ ότι κάποιος
με αναγκάζει να κάνω κάτι συγκεκριμένο,
πιστεύω ότι κανείς δε σκέφτηκε να
επικοινωνήσει μαζί του. Δηλαδή έτσι ελπίζω.
</p>

EOHTML;
}

function get_story_people_solution_2_en() {
  return <<<EOHTML

<p>
The sadly controversial part
is that better communication is not just a legend,
there are companies that communicate well with
all their members and clients and business people,
and that's documented and talked about.
I know the manifesto for agile software development
has a bad name, but I've been into drawing and
I think of it like
high level art advice by old artists.
I'm exclusively referring to the original writings,
such as the manifesto itself.
I was learning illustration in the Draw A Box
community and Stanislav Prokopenko's videos,
and it was impossible to interpret advice alone.
Advice and exercises were two separate ideas.
But anyhow, high level advice hints what an ending
to the journey can be like,
and what I get out of Fowler's speeches is that
I should absolutely believe in an environment
where the developers are treated with respect.
</p>

EOHTML;
}

function get_story_people_solution_2_gr() {
  return <<<EOHTML

<p>
Το θέμα είναι ότι υπάρχουν εταιρείες που τα
μέλη να επικοινωνούν καλά, πελάτες και
προγραμματιστές και επιχειρηματίες.
Υπάρχουν έγγραφα και γίνονται ομιλίες στο θέμα.
Καταλαβαίνω ότι θέματα όπως το
manifesto for agile software development
έχουν κακή φήμη, για σοβαρούς λόγους,
αλλά έχω ασχοληθεί με ζωγραφική και
τα αντιστοιχίζω με συμβουλές προχωρημένων καλλιτεχνών.
Αναφέρομαι αποκλειστικά στα αυθεντικά έγγραφα,
όπως το ίδιο το μανιφέστο.
Μάθαινα σκιτσογραφία με την κοινότητα Draw A Box
και με βίντεο του Stanislav Prokopenko,
και ήταν αδύνατο να ερμηνεύσω συμβουλές μόνος μου.
Συμβουλές και ασκήσεις ήταν ξεχωριστές έννοιες.
Σε κάθε περίπτωση, οι συμβουλές υψηλού επιπέδου
δίνουν μια εικόνα του τέλους ενός ταξιδιού,
και αυτό που θα έπαιρνα από τις ομιλίες του
Martin Fowler είναι ότι υπάρχουν και θα υπάρξουν
περιβάλλοντα που σέβονται τους προγραμματιστές.
EOHTML;
}

